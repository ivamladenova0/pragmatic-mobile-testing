package enums;

/**
 * Swipe direction enum.
 */
public enum SwipeDirection {
    /**
     * Up from the center of the lower.
     */
    UP,
    /**
     * Down from the center of the upper.
     */
    DOWN,
    /**
     * To the left from the center of the rightmost.
     */
    LEFT,
    /**
     * To the right from the center of the leftmost.
     */
    RIGHT;
}
